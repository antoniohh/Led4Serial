    /**
     * Método conectar().
     *
     * Realiza la conexión al puerto serie seleccionado en el combobox.
     */
    public synchronized void conectar() {
        try {
            
            // Comprobamos que todos los parametros del puerto estén completos.
            if ("".equals(jTextField2.getText()) | "".equals(jTextField3.getText())
                    | "".equals(jTextField1.getText()) | "".equals(jTextField9.getText())
                    | "".equals(jTextField10.getText())) {
                JOptionPane.showMessageDialog(this, "Introduzca todos los datos "
                        + "del puerto serie.");
                return;
            }

            // Instanciamos un objeto de la clase puerto con los datos del puerto.
            this.puerto = new Puerto(jTextField1.getText(),
                    Integer.parseInt(jTextField3.getText()),
                    Integer.parseInt(jTextField2.getText()),
                    Integer.parseInt(jTextField9.getText()),
                    Integer.parseInt(jTextField10.getText())
            );
            
            /**
             * Recorremos el array string de puertos y paramos en el seleccionado.
             * 
             * String[] lista = SerialPortList.getPortNames();          
             * for (String lp : lista) {
             *     if (lp.equals(PORT_NAME)) {
             *         PORT_ID = lp;
             *         break;
             *     }                
             * }
             */
            
            // Abrimos el puerto serie seleccionado.
            puertoSerie = new SerialPort(puerto.getPortId());
            puertoSerie.openPort();

            /**
             * Configuramos los parametros del puerto.
             * 
             * string: serialPort.setParams(9600, 8, 1, 0);
             * 
             * puertoSerie.setParams(SerialPort.BAUDRATE_9600, 
             *                      SerialPort.DATABITS_8,
             *                      SerialPort.STOPBITS_1,
             *                      SerialPort.PARITY_NONE);
             */            
            puertoSerie.setParams(puerto.getBaudrate(),
                    puerto.getDataBits(),
                    puerto.getStopBits(),
                    puerto.getParity()
            );
            
            // Añadimos los listeners.
            puertoSerie.addEventListener(this);
            
            // Mensaje de exito.
            System.out.println("Se ha establecido la conexión con el puerto serie "
                    + puerto.getPortId() + "."
            );
            JOptionPane.showMessageDialog(this, "Se ha establecido la conexión "
                    + "con el puerto serie " + puerto.getPortId() + "."
            );

            // Habilitamos los botones de los leds al tener una conexión correcta.
            if (puertoSerie != null) {
                jButton3.setEnabled(true);
                jButton4.setEnabled(true);
                jButton5.setEnabled(true);
                jButton6.setEnabled(true);
                jButton7.setEnabled(true);
                jButton8.setEnabled(true);
                jButton9.setEnabled(true);
                jButton10.setEnabled(true);
                jButton11.setEnabled(true);
                jButton12.setEnabled(true);
            }
        } 
        catch (SerialPortException ex) {
            System.err.println(ex.toString());
            
            // Log fatal de conexión.
            LOG.fatal("Error en la conexión al puerto serie: ",ex);

            // Mensaje de fallo.
            JOptionPane.showMessageDialog(this, "No se ha podido establecer la "
                    + "conexión con el puerto serie "+puerto.getPortId()+"."
            );
        }
    }

    /**
     * Método cerrar().
     *
     * Cierra la conexión serie creada, liberando el puerto.
     * @throws jssc.SerialPortException
     */
    public synchronized void cerrar() throws SerialPortException {
        try {
            if (puertoSerie != null) {
                puertoSerie.removeEventListener();
                puertoSerie.closePort();
                System.out.println("Se ha cerrado la conexión con el puerto serie"
                        + " "+" "+puerto.getPortId()+".");
            } 
        }
        catch (SerialPortException ex) {
            System.err.println(ex.toString());
            
            // Log error de conexión.
            LOG.error("Error al cerrar el puerto serie: ",ex);
        }
    }

    /**
     * Método enviar(String cadena).
     *
     * Envia una cadena de texto al dispositivo Arduino.
     *
     * @param cadena
     * @throws jssc.SerialPortException
     */
    public synchronized void enviar(String cadena) throws SerialPortException {
        try {
            puertoSerie.writeBytes(cadena.getBytes());
            System.out.println("Enviado a Arduino: " + cadena);
            dao.insertarMensaje(cadena);
        }
        catch (SerialPortException ex) {
            System.err.println(ex.toString());
            
            // Log error de conexión.
            LOG.error("Error al enviar datos: ",ex);
        }
    }

    /**
     * Método serialEvent(SerialPortEvent evento).
     *
     * Recibe una cadena de texto enviada por el dispositivo Arduino y la
     * muestra en el campo de texto asignado a tal fin.
     *
     * @param evento
     */
    @Override
    public synchronized void serialEvent(SerialPortEvent evento) {
        if(evento.isRXCHAR()){    
            if(evento.getEventValue() > 0){
                try {
                    wait(100);
                    String cadena="";
                    byte buffer[] = puertoSerie.readBytes();
                    for (byte bb : buffer) {
                        cadena = cadena + (char) bb;         
                    }
                    System.out.println("Recibido de Arduino: " + cadena);
                    jlista2Modelo.addElement(cadena);
                    dao.insertarMensaje(cadena);                 
                    if (cadena.contains("Azul")) {
                        if (cadena.contains("Encendido")) jLabel4.setIcon(this.on);
                        else if (cadena.contains("Apagado")) jLabel4.setIcon(this.off);
                        jTextField4.setText(cadena);
                    }
                    else if (cadena.contains("verde")) {
                        if (cadena.contains("Encendido")) jLabel5.setIcon(this.on);
                        else if (cadena.contains("Apagado")) jLabel5.setIcon(this.off);
                        jTextField5.setText(cadena);
                    }
                    else if (cadena.contains("rojo")) {
                        if (cadena.contains("Encendido")) jLabel6.setIcon(this.on);
                        else if (cadena.contains("Apagado")) jLabel6.setIcon(this.off);
                        jTextField6.setText(cadena);
                    }
                    else if (cadena.contains("amarillo")) {
                        if (cadena.contains("Encendido")) jLabel7.setIcon(this.on);
                        else if (cadena.contains("Apagado")) jLabel7.setIcon(this.off);
                        jTextField7.setText(cadena);
                    }
                    else if (cadena.contains("Todos")) {
                        if (cadena.contains("Encendidos")) jLabel4.setIcon(this.on);
                        else if (cadena.contains("Apagados")) jLabel4.setIcon(this.off);
                        jTextField8.setText(cadena);
                    }
                }
                catch (InterruptedException | SerialPortException ex) {
                    System.out.println(ex);
                    
                    // Log error de conexión.
                    LOG.error("Error al recibir datos: ",ex);
                }
            }
        }
    }
    
    public synchronized void consultarMensajes() {
        try {
            jlistaModelo.removeAllElements();
            this.lista = dao.leerMensaje();            
            for (int i=0;i<=lista.size();i++){
                jlistaModelo.addElement(this.lista.get(i).toString());
            }            
        }
        catch (Exception ex) {
            System.out.println(ex);

            // Log info de conexión.
            LOG.info("No existen datos.: ",ex);           
        }
    }

